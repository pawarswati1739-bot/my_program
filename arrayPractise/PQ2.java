package com.arrayPractise;

import java.util.Scanner;

//using scanner print array
public class PQ2 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int[] no = new int[0];
		for (int i = 0; i < no.length; i++) {
			no[i] = sc.nextInt();
		}
		for (int i = 0; i < no.length; i++) {
			System.out.println(no[i]);
		}
	}
}
